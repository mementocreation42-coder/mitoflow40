export function getNutrientBySlug(slug: string): Nutrient | undefined {
    return nutrients.find((n) => n.slug === slug);
}

// 一覧での表示カテゴリ順
export const NUTRIENT_CATEGORY_ORDER = [
    'タンパク質・アミノ酸',
    'ビタミン',
    'ビタミンB群',
    'ミネラル',
    '脂質',
    'その他',
];

// カテゴリ別にグループ化（定義順）
export function getNutrientsByCategory(): { category: string; items: Nutrient[] }[] {
    return NUTRIENT_CATEGORY_ORDER
        .map((category) => ({
            category,
            items: nutrients.filter((n) => n.category === category),
        }))
        .filter((g) => g.items.length > 0);
}
