export function getFoodBySlug(slug: string): Food | undefined {
    return foods.find((f) => f.slug === slug);
}

export function getFoodsByCategory(): { category: string; items: Food[] }[] {
    return FOOD_CATEGORY_ORDER.map((category) => ({
        category,
        items: foods.filter((f) => f.category === category),
    })).filter((g) => g.items.length > 0);
}

// ある栄養素を多く含む食材を引く（栄養素ページからの逆引き用）
export function getFoodsByNutrient(nutrientSlug: string): Food[] {
    return foods.filter((f) => f.nutrients.includes(nutrientSlug));
}
