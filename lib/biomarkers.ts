export function getBiomarkerBySlug(slug: string): Biomarker | undefined {
    return biomarkers.find((b) => b.slug === slug);
}

// カテゴリ別にグループ化
export function getBiomarkersByCategory(): Record<string, Biomarker[]> {
    return biomarkers.reduce((acc, b) => {
        if (!acc[b.category]) acc[b.category] = [];
        acc[b.category].push(b);
        return acc;
    }, {} as Record<string, Biomarker[]>);
}
